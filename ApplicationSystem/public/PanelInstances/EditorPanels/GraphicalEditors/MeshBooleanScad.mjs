// Shared browser-side bridge for mesh boolean operations rendered through the server OpenSCAD route.

import { STLLoader } from "/lib/three/STLLoader.js";

const BOOLEAN_OPERATIONS = new Set(["union", "difference", "intersection"]);
const DEFAULT_WELD_EPSILON = 1e-5;
const MAX_SCAD_BOOLEAN_SOURCE_BYTES = 4_800_000;

export function normalizeMeshBooleanOperation(operation) {
  const key = String(operation || "").trim().toLowerCase();
  return BOOLEAN_OPERATIONS.has(key) ? key : null;
}

export function meshBooleanLabel(operation) {
  const key = normalizeMeshBooleanOperation(operation) || "boolean";
  return key.charAt(0).toUpperCase() + key.slice(1);
}

function numberLiteral(value) {
  const n = Number(value);
  if (!Number.isFinite(n)) return "0";
  const rounded = Number(n.toFixed(6));
  return Object.is(rounded, -0) ? "0" : String(rounded);
}

function pointArray(point) {
  if (Array.isArray(point)) return [Number(point[0]), Number(point[1]), Number(point[2])];
  if (point && typeof point === "object") return [Number(point.x), Number(point.y), Number(point.z)];
  return [NaN, NaN, NaN];
}

function faceArray(face, vertexCount) {
  if (!Array.isArray(face)) return null;
  const indices = face.map((index) => Number(index)).filter((index) => Number.isInteger(index) && index >= 0 && index < vertexCount);
  if (indices.length < 3 || new Set(indices).size < 3) return null;
  return indices;
}

function indentBlock(text, spaces = 2) {
  const pad = " ".repeat(spaces);
  return String(text || "").split("\n").map((line) => line ? pad + line : line).join("\n");
}

export function meshOperandToScadPolyhedron(operand = {}) {
  const points = Array.isArray(operand.vertices) ? operand.vertices.map(pointArray) : [];
  const validPoints = points.filter((point) => point.length === 3 && point.every(Number.isFinite));
  if (validPoints.length < 3) throw new Error("Boolean operand has fewer than three valid vertices.");

  const faces = (Array.isArray(operand.faces) ? operand.faces : [])
    .map((face) => faceArray(face, validPoints.length))
    .filter(Boolean);
  if (!faces.length) throw new Error("Boolean operand has no valid faces.");

  const pointSource = validPoints.map((point) => "[" + point.map(numberLiteral).join(", ") + "]").join(", ");
  const faceSource = faces.map((face) => "[" + face.join(", ") + "]").join(", ");
  return "polyhedron(points = [" + pointSource + "], faces = [" + faceSource + "], convexity = 10);";
}

export function buildBooleanScad(operation, operands = []) {
  const key = normalizeMeshBooleanOperation(operation);
  if (!key) throw new Error("Unsupported boolean operation.");
  const renderedOperands = operands.map(meshOperandToScadPolyhedron);
  if (renderedOperands.length < 2) throw new Error("Select at least two closed mesh solids for a boolean operation.");

  const source = [
    "// Generated by Nodevision mesh boolean tools.",
    "// The source mesh remains client-side; only this transient SCAD body is rendered.",
    "$fn = 32;",
    key + "() {",
    renderedOperands.map((entry) => indentBlock(entry, 2)).join("\n"),
    "}",
    "",
  ].join("\n");

  if (new TextEncoder().encode(source).byteLength > MAX_SCAD_BOOLEAN_SOURCE_BYTES) {
    throw new Error("Selected meshes are too large for the current OpenSCAD boolean bridge.");
  }
  return source;
}

export async function renderBooleanScadToArrayBuffer(operation, operands = []) {
  const scadCode = buildBooleanScad(operation, operands);
  const response = await fetch("/api/scad/render", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ scadCode, format: "stl" }),
  });

  if (!response.ok) {
    const json = await response.json().catch(() => null);
    const text = json ? "" : await response.text().catch(() => "");
    const details = [json?.error || text || `${response.status} ${response.statusText}`, json?.hint]
      .filter(Boolean)
      .join("\n");
    throw new Error(details || "Mesh boolean render failed.");
  }

  return await response.arrayBuffer();
}

export function topologyFromBufferGeometry(THREE, geometry, options = {}) {
  const weldEpsilon = Number.isFinite(options.weldEpsilon) && options.weldEpsilon > 0 ? options.weldEpsilon : DEFAULT_WELD_EPSILON;
  const nonIndexed = geometry?.index ? geometry.toNonIndexed() : geometry;
  const pos = nonIndexed?.getAttribute?.("position");
  if (!pos || !Number.isFinite(pos.count) || pos.count === 0) return { vertices: [], faces: [], customEdges: [] };

  const vertices = [];
  const faces = [];
  const weldMap = new Map();
  const quantize = (n) => Math.round(Number(n || 0) / weldEpsilon);

  for (let i = 0; i < pos.count; i += 3) {
    const tri = [];
    for (let j = 0; j < 3; j += 1) {
      const idx = i + j;
      const x = pos.getX(idx);
      const y = pos.getY(idx);
      const z = pos.getZ(idx);
      const key = `${quantize(x)}|${quantize(y)}|${quantize(z)}`;
      let vIndex = weldMap.get(key);
      if (vIndex === undefined) {
        vIndex = vertices.length;
        vertices.push(new THREE.Vector3(x, y, z));
        weldMap.set(key, vIndex);
      }
      tri.push(vIndex);
    }
    if (new Set(tri).size === 3) faces.push(tri);
  }

  if (nonIndexed !== geometry) nonIndexed.dispose?.();
  return { vertices, faces, customEdges: [] };
}

export function topologyFromSTLArrayBuffer(THREE, arrayBuffer, options = {}) {
  const geometry = new STLLoader().parse(arrayBuffer);
  try {
    return topologyFromBufferGeometry(THREE, geometry, options);
  } finally {
    geometry?.dispose?.();
  }
}

export async function renderBooleanOperationToTopology(THREE, operation, operands = [], options = {}) {
  const arrayBuffer = await renderBooleanScadToArrayBuffer(operation, operands);
  return topologyFromSTLArrayBuffer(THREE, arrayBuffer, options);
}
